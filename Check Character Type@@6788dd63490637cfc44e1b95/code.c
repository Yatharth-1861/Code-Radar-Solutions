#include <stdio.h>

int main() {
    char ch;
    scanf("%c",&ch);
    if(ch>=0 && ch<=9){
        printf("Digit");
    }
    else if(ch!=a,i,e,o,u){
        printf("Consonant");
    }
    else if(ch==a,e,i,o,u){
        printf("Vovel");
    }
    else{
        printf("Special Character");
    }
    return 0;
}